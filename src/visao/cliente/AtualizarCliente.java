    private void tabelaDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaDadosMouseClicked
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente atualizar esse registro?");
        
        if(resposta == 0){
            
            Cliente cliente = new Cliente();
            int linha = tabelaDados.getSelectedRow();
            cliente.setNomeCompleto(tabelaDados.getValueAt(linha, 0).toString());
            cliente.setCpf(tabelaDados.getValueAt(linha, 1).toString());
            cliente.setEndereco(tabelaDados.getValueAt(linha, 2).toString());
            cliente.setBairro(tabelaDados.getValueAt(linha, 3).toString());
            cliente.setCidade(tabelaDados.getValueAt(linha, 4).toString());
            cliente.setUf(tabelaDados.getValueAt(linha, 5).toString());
            cliente.setCep(tabelaDados.getValueAt(linha, 6).toString());
            cliente.setTelefone(tabelaDados.getValueAt(linha, 7).toString());
            cliente.setEmail(tabelaDados.getValueAt(linha, 8).toString());
            
            new AtualizarFormulario(cliente).setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_tabelaDadosMouseClicked

