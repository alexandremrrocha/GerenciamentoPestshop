    public AtualizarFormulario(Produto produto) {
        initComponents();
        
        String[] auxDataVetor = CProduto.converteDataString(produto.getDataVencimento())
                .split("/");
        String auxDataVencimento = "";
        
        for(int i = 0; i < auxDataVetor.length; i++){
            auxDataVencimento += auxDataVetor[i];
        }
        
        campoCodigo.setText(produto.getCodigoProduto());
        campoNomeProduto.setText(produto.getNomeProduto());
        campoFornecedor.setText(produto.getFornecedor());
        campoMarca.setText(produto.getMarca());
        campoValorCusto.setText(produto.getValorCusto());
        campoValorVenda.setText(produto.getValorVenda());
        campoQuantidade.setText(produto.getQuantidade());
        campoDataValidade.setText(auxDataVencimento);
         
    }

